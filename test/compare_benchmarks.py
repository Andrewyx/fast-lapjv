#!/usr/bin/env python3
import os
import json
import argparse
import datetime
import glob
import sys

# When running under Bazel, we need to resolve the actual workspace directory
# to save our results persistently, rather than saving them in the ephemeral runfiles tree.
WORKSPACE_ROOT = os.environ.get('BUILD_WORKSPACE_DIRECTORY', os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
RESULTS_DIR = os.path.join(WORKSPACE_ROOT, "results")

def get_latest_result():
    if not os.path.exists(RESULTS_DIR):
        return None
    
    files = glob.glob(os.path.join(RESULTS_DIR, "benchmark_*.json"))
    if not files:
        return None
    
    # Sort by filename (which includes timestamp)
    latest_file = sorted(files)[-1]
    with open(latest_file, 'r') as f:
        return json.load(f), latest_file

def compare_results(old_data, new_data):
    old_benchmarks = {b['name']: b for b in old_data.get('benchmarks', [])}
    new_benchmarks = {b['name']: b for b in new_data.get('benchmarks', [])}
    
    print(f"\n{'Benchmark Name':<35} | {'Old Time (ns)':<15} | {'New Time (ns)':<15} | {'Change (%)':<10}")
    print("-" * 85)
    
    for name, new_b in new_benchmarks.items():
        if name in old_benchmarks:
            old_b = old_benchmarks[name]
            
            # Use real_time for comparison
            if 'real_time' in old_b and 'real_time' in new_b:
                old_time = old_b['real_time']
                new_time = new_b['real_time']
                
                diff_pct = ((new_time - old_time) / old_time) * 100
                
                color = ""
                reset = ""
                if diff_pct > 5: # slower
                    color = "\033[91m" # Red
                    reset = "\033[0m"
                elif diff_pct < -5: # faster
                    color = "\033[92m" # Green
                    reset = "\033[0m"
                    
                print(f"{name:<35} | {old_time:<15.2f} | {new_time:<15.2f} | {color}{diff_pct:>+8.2f}%{reset}")
            else:
                print(f"{name:<35} | {'N/A':<15} | {'N/A':<15} | {'N/A':<10}")
        else:
             print(f"{name:<35} | {'---':<15} | {new_b.get('real_time', 'N/A'):<15.2f} | {'NEW':<10}")

def save_result(data):
    os.makedirs(RESULTS_DIR, exist_ok=True)
    timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = os.path.join(RESULTS_DIR, f"benchmark_{timestamp}.json")
    with open(filename, 'w') as f:
        json.dump(data, f, indent=2)
    print(f"\nSaved new results to {filename}")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Compare LAPJV benchmark results.")
    parser.add_argument("benchmark_file", help="Path to the JSON output file generated by Google Benchmark")
    args = parser.parse_args()

    if not os.path.exists(args.benchmark_file):
        print(f"Error: Provided benchmark file '{args.benchmark_file}' does not exist.")
        sys.exit(1)

    with open(args.benchmark_file, 'r') as f:
        try:
            new_data = json.load(f)
        except json.JSONDecodeError as e:
            print(f"Error parsing provided JSON file: {e}")
            sys.exit(1)
    
    latest_result = get_latest_result()
    if latest_result:
        old_data, old_file = latest_result
        print(f"\nComparing with previous results from {os.path.basename(old_file)}:")
        compare_results(old_data, new_data)
    else:
        print("\nNo previous results found to compare against.")
        
    save_result(new_data)
